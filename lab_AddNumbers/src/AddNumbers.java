public class AddNumbers {
	public static void main(String[ ] args) {
		int x, y, z, p;
		x = 1; //Sets x to 1
		y = 10; //Sets y to 10
		z = x + y; //Sets z to the sum of x and y
		p = x * y; //Sets p to the product of x and y
		System.out.println("The first value is: " + x); //Prints x
		System.out.println("The second value is: " + y); //Prints y
		System.out.println("The sum of the values is: " + z); //Prints z
		System.out.println("The product of the values is: " + p); //Prints p
	}
}